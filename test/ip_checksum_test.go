package test

import (
	"exu"
	"testing"
)

func TestIpv4Checksum(t *testing.T) {
	packet := []byte{0x4e, 0x07, 0x30, 0x56, 0xc0, 0x6d, 0x42, 0x69, 0x33, 0x90, 0xaa, 0x56, 0x08, 0x00, 0x45, 0x00,
		0x00, 0x54, 0x00, 0x00, 0x00, 0x00, 0x40, 0x01, 0xf4, 0x19, 0x0a, 0x00, 0x00, 0x01, 0x0a, 0x00,
		0x00, 0x02, 0x00, 0x00, 0x68, 0x76, 0x00, 0x18, 0x00, 0x16, 0x57, 0xb8, 0xad, 0x65, 0x00, 0x00,
		0x00, 0x00, 0xc9, 0x6a, 0x0a, 0x00, 0x00, 0x00, 0x00, 0x00, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15,
		0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x20, 0x21, 0x22, 0x23, 0x24, 0x25,
		0x26, 0x27, 0x28, 0x29, 0x2a, 0x2b, 0x2c, 0x2d, 0x2e, 0x2f, 0x30, 0x31, 0x32, 0x33, 0x34, 0x35,
		0x36, 0x37}

	frame := exu.EthernetFrame{}
	err := frame.FromBytes(packet)
	if err != nil {
		t.Error(err)
	}

	ipv4 := exu.IPv4Packet{}
	err = ipv4.UnmarshalBinary(frame.Payload())
	if err != nil {
		t.Error(err)
	}

	checksum := ipv4.Header.CalculateChecksum()
	if checksum != 0x66a7 {
		t.Errorf("Expected checksum 0x66a7, got 0x%x", checksum)
	}
}
